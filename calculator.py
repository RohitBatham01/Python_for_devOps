num1=input("Enter first no.: ")  # take input from user
num2=input("Enter second no. :")  # take input from user
print(type(num1))  # print the type of input value
print(type(num2))  # print the type of input value

sum_of_numbers=float(num1)+float(num2)  # add two numbers after converting string to float   Type Casting
print("Sum of two numbers is:", sum_of_numbers)  # print the sum




diff_of_numbers=float(num1)-float(num2)  # subtract two numbers after converting string to float   Type Casting
print("Difference of two numbers is:", diff_of_numbers)  # print the difference


product_of_numbers=float(num1)*float(num2)  # multiply two numbers after converting string to float   Type Casting
print("Product of two numbers is:", product_of_numbers)  # print the product


division_of_numbers=float(num1)/float(num2)  # divide two numbers after converting string to float   Type Casting
print("Division of two numbers is:", division_of_numbers)  # print the division